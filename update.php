<?php
// Include database connection file
require_once "database/connection.php";
require_once "validations/validation.php";
 
// Define variables and initialize with empty values
$name = $description = $category = $price = "";
$name_err = $description_err = $category_err = $price_err = "";
 
// Processing form data when form is submitted
if(isset($_POST["id"]) && !empty($_POST["id"])){
    // Get hidden input value
    $id = $_POST["id"];
    
    $input_name = trim($_POST["name"]);
    list($name, $name_err) = validateName($input_name);
    
    $input_description = trim($_POST["description"]);
    list($description, $description_err) = validateDescription($input_description);

    $category = trim($_POST["category"]);
    list($category, $category_err) = validateCategory($category);

    $price = trim($_POST["price"]);
    list($price, $price_err) = validatePrice($price);
    
    // Check input errors before inserting in database
    if(empty($name_err) && empty($description_err) && empty($category_err) && empty($price_err)){
      require_once "mysql/update.php";
      updateDb($mysqli, $name, $description, $price, $category, $id);
    }
    
    // Close connection
    $mysqli->close();
} else{
    // Check existence of id parameter before processing further
    if(isset($_GET["id"]) && !empty(trim($_GET["id"]))){
        // Get URL parameter
        $id =  trim($_GET["id"]);
        
        // Prepare a select statement
        $sql = "SELECT * FROM SimpleProducts WHERE ID = ?";
        if($stmt = $mysqli->prepare($sql)){
            // Bind variables to the prepared statement as parameters
            $stmt->bind_param("i", $param_id);
            
            // Set parameters
            $param_id = $id;
            
            // Attempt to execute the prepared statement
            if($stmt->execute()){
                $result = $stmt->get_result();
                
                if($result->num_rows == 1){
                    /* Fetch result row as an associative array. Since the result set
                    contains only one row, we don't need to use while loop */
                    $row = $result->fetch_array(MYSQLI_ASSOC);
                    
                    // Retrieve individual field value
                    $name = $row["Name"];
                    $description = $row["Description"];
                    $category = $row["Category"];
                    $price = $row["Price"];
                } else{
                    // URL doesn't contain valid id. Redirect to error page
                    header("location: error.php");
                    exit();
                }
                
            } else{
                echo "Oops! Something went wrong. Please try again later.";
            }
        }
        
        // Close statement
        $stmt->close();
        
        // Close connection
        $mysqli->close();
    }  else{
        // URL doesn't contain id parameter. Redirect to error page
        header("location: error.php");
        exit();
    }
}
